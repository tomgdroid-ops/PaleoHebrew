// Gloss Cleanup Function (for entries not in the KJV map)
// ============================================================

function cleanupGloss(gloss) {
  if (!gloss) return "—";

  let cleaned = gloss;

  // Remove common dictionary prefixes
  const prefixes = [
    "properly, ", "abstractly, ", "concretely, ",
    "abstract, ", "concrete, ", "figuratively, ",
    "specifically, ", "generally, ", "by implication, ",
    "in a literal and", "literally, ",
  ];
  for (const prefix of prefixes) {
    if (cleaned.toLowerCase().startsWith(prefix)) {
      cleaned = cleaned.slice(prefix.length).trim();
    }
  }

  // Take text before first semicolon or "i.e." or long comma clause
  cleaned = cleaned.split(';')[0].trim();
  cleaned = cleaned.split(' i.e.')[0].trim();
  cleaned = cleaned.split(' (')[0].trim();

  // If still long, take first phrase before comma (but only if result is reasonable)
  if (cleaned.length > 20) {
    const commaIdx = cleaned.indexOf(',');
    if (commaIdx > 0 && commaIdx < 25) {
      cleaned = cleaned.slice(0, commaIdx).trim();
    }
  }

  // Remove trailing articles and prepositions left hanging
  cleaned = cleaned.replace(/\s+(a|an|the|of|or|and|in|to|by|as|is)\s*$/i, '');

  // Remove {braces} content
  cleaned = cleaned.replace(/\{[^}]*\}/g, '').trim();

  // Truncate to 25 chars max
  if (cleaned.length > 25) {
    const spaceIdx = cleaned.lastIndexOf(' ', 24);
    if (spaceIdx > 8) {
      cleaned = cleaned.slice(0, spaceIdx);
    } else {
      cleaned = cleaned.slice(0, 25);
    }
  }

  // Capitalize first letter
  if (cleaned.length > 0) {
    cleaned = cleaned.charAt(0).toUpperCase() + cleaned.slice(1);
  }

  return cleaned || "—";
}

